void move_servo(uint32_t num) {
    if (num > 100) num = 150; // upper clamp, this will tilt towards the servo side
    if (num < 0) num = 0; //lower clamp, this will tilt towards the sensor side
    pulse_width = 700 + ((uint32_t)num * 550) / 100;
    __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, pulse_width);
}

void pid(void){
	error = setpoint - distance;
	prev_error = setpoint - prev_distance;

	// filter for noise
	error = (0.53 * error) + (0.47 * prev_error);

	// calculate propotional part
	pid_p = kp * error;

	// capping the effect of proportional part
	if(pid_p > 25) pid_p = 25;
	if(pid_p < -25) pid_p = -25;

	// calculate derivative part
	pid_d = kd/50.0 * (error - prev_error); // time is in ms
	// filter for noise
	pid_d = (0.55 * pid_d) + (0.45 * prev_pid_d);

	// calculate integral part
	pid_i = pid_i + ki * error;
	// filter for noise
	pid_i = (0.55 * pid_i) + (0.45 * prev_pid_i);

	// sum all parts to get output
	pid_total = pid_p + pid_d + pid_i;

	// reduce effect when close to the setpoint
	if(error < 10){
				pid_total /= 3.0;
				pid_i = 0;
			}

	// set a max range for pid control (due to servo limits)
	if(pid_total > 25){
		pid_total = 25;
	}

	// update the previous values
	prev_pid_d = pid_d;
	prev_pid_i = pid_i;
}
